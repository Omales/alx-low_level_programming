#include <stdio.h>
/**
 * main - that prints all single digit numbers of base
 * Description: using the main function
 * this program that prints all single digit numbers of base
 * Return: 0
 */

int main(void)

{

	char c;

	for (c = '0'; c <= '9'; c++)

	{

		putchar(c);

	}

	putchar('\n');

	return (0);

}
